            <section id="hvad-er-multimediedesign">
                <h2 class="overskrift">Hvad er multimediedesign?</h2>
                <p class="body-text">
                    Planlægning og bevidst anvendelse af mange forskellige redskaber, som tekst, billeder, videoer, grafik osv. til at udtrykke og kommunikere et vist budskab til modtageren (Busch, 2015).
                </p>
                <p class="body-text">
                    At man kan tage de forskellige redskaber som kode, billeder og tekst og putter dem samme/integrere dem så de som en større enhed kan udtrykke noget som de ikke kan hver for sig (ibid). 
                </p>
            </section> <!-- hvad er spøreskemaer -->
            
            <section class="typiske-emner">
                
                <h2 class="overskrift">De 5 grundantagelser</h2>
                
                <ol class="liste">
                    <li>
                        <h3 class="underoverskrift" > Man kan ikke ikke-kommunikere</h3>
                        <p class="body-text">
                            Denne grundantagelse siger at al adfærd er kommunikation, hvad du siger, hvad du gøre, hvad du ikke siger, hvad du ikke gøre osv. (Andersen &#38; Jørnø, 2013). Når du er sammen med andre mennesker, vil du altid have en adfærd, som de andre kan opfatte (ibid). For eksempel vil fremmede mennesker, der venter på bussen ved et stoppested, registrere og tolke hinanden, selvom de ikke taler sammen (ibid). Kommunikation foregår konstant (ibid).
                        </p>
                    </li>
                    
                    <li>
                        <h3 class="underoverskrift" >Enhver kommunikation består af to dele: selve indholdet og oplysninger om det indbyrdes forhold, der er mellem dem, der kommunikerer.</h3>
                        <p class="body-text">
                            Denne opfattelse siger at når man kommunikerer er der to sider man kigger på (Andersen &#38; Jørnø, 2013). Den ene side er indholdet af kommunikationen og den anden side er oplysninger om det indbyrdes forhold mellem dem der kommunikerer (ibid). Det vil sige at når man kommunikerer så har opfattelse du har af en person og personens opfattelse af dig osv. en betydning for hvordan i kommunikere med hinanden og forstår hinanden (ibid). For eksempel så vil ordet ”idiot” være opfattet som en joke hvis du sagde det til dine venner, men hvis du siger det til en fremmede eller en medarbejde vil det nok ses på en mere negativ måde (ibid).
                        </p>
                    </li>
                    
                    <li>
                        <h3 class="underoverskrift" >Den måde, vi udtrykker os på, og den måde, vi tolker hinandens kommunikation på, afhænger også af det forhold vi har til hinanden.</h3>
                        <p class="body-text">
                            Denne antagelse siger at den måde vi udtrykker os på, har en indflydelse på hvordan vi tolker hinanden (Andersen &#38; Jørnø, 2013).  Ved hjælp af pauser og betoninger i det talte sprog forstår vi, hvile dele af informationerne der hænger sammen og på hvilken måde (ibid). Modtageren opfatter ikke altid afsenderens anvisninger, men tolker dem ud fra deres egen opfattelse af afsenderen (ibid).
                        </p>
                    </li>
                    
                    <li>
                        <h3 class="underoverskrift" >Mennesker kommunikerer både med en digital kode og en analog kode</h3>
                        <p class="body-text">
                            Denne antagelse taler om to former for sprogkode:
                        </p>
                        <ul class="liste">
                            <li>
                                Den digitale sprogkode: Det vi konkret skriver eller siger (Andersen &#38; Jørnø, 2013).
                            </li>
                            <li>
                                Den analoge sprogkode: Tonefald og kropssprog eller lignende (ibid).
                            </li>
                        </ul>
                        <p class="body-text">
                            Disse to sprogkoder har indflydelse på hvordan folk forstår det du mener når du kommunikerer (ibid). Derfor er det vigtigt at når man kommunikere at både den digitale og analoge sprogkode hænger sammen eller har kontrast på en hensigtsmæssig måde (ibid). Hvis det er talt, vil det være mimikere eller betoninger lignende (ibid). Når det er skrift, vil det være valg af papirfarve, skrift, opsætning på siden, emojis osv. (ibid).
                        </p>
                    </li>
                    
                    <li>
                        <h3 class="underoverskrift" >Kommunikationsudveksling er enten symmetrisk eller asymmetrisk afhængigt af, om den hviler på lighed eller forskel i magt.</h3>
                        <p class="body-text">
                            Denne opfattelse siger at kommunikationen mellem mennesker er afhængig af om der er lighed eller forskel i magt mellem parterne (Andersen &#38; Jørnø, 2013). Her er der tale om:
                        </p>
                        
                        <h4 class="sub-skrift">
                            Symmetrisk kommunikation: 
                        </h4>
                        <ul class="liste" id="syme">
                            <li>
                                Det kan være formelt, hvor man taler med en kollega med samme stillingsniveau (ibid).
                            </li>
                            <li>
                                Det kan være uformelt, hvor man taler med sin ven (ibid).
                            </li>
                        </ul>
                        
                        <h4 class="sub-skrift">
                            Asymmetrisk kommunikation: 
                        </h4>
                        <ul class="liste" id="liste-asym">
                            <li>
                                Det kan være man taler med sin chef på jobbet så har chefen mere magt og måden du vil kommunikere og han vil kommunikere vil være forskellig (ibid).
                            </li>
                            <li>
                                Det kan være du taler med din ven som er fantastisk til at designe om noget ser godt ud eller dårligt ud (ibid). Hvor din ven automatisk vil have mere magt fordi de har mere viden, men det kan være symmetrisk hvis man har aftalt det som værende en samtale på lige fod (ibid).
                            </li>
                        </ul>
                    </li>
                </ol>
