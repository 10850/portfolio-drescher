                    De fem principper til et godt powerpoint
                </h2>
                <p class="body-text">
                    For at kunne holde en god tale skal man vide hvordan man laver et godt PowerPoint (Phillips, 2014). Et dårligt PowerPoint kan ende med at formindske folks opmærksomhed til hvad dit budskab er og derfor ende med at du ender i fælden ”Death by PowerPoint” (ibid). Her præsenter David JP Phillips fem simple principper til et godt PowerPoint (ibid):
                </p>
