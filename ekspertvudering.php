            <section class="heruistikker">
                <h2 class="overskrift">Herustikker</h2>
                
                <p class="body-text">
                    I ekspertvurderingen er det et godt udgangspunkt at gå ud fra usability-specialisten Jakob Nielsens 10 heuristikker, som er de mest anvendte og anerkendte (Gregersen &#38; Wisler-Poulsen, 2017).
                </p>
                
                
                <div class="background-image-usability">
                    <table class="usability-table">
                        <tr>
                            <th>1. Systemstatus </th>
                            <td>
                                Kommunikation med brugeren i forhold til der nuværende placering på siden eller direkte meddelelser brugeren får på websitet som bekræftelser (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>2. Sammenhæng mellem website og den virkelige verden </th>
                            <td>
                                Sproget på siden burde være virkelighedsnære med ord og vendinger som brugerne kan forstå og ikke tekniske og formelle ord som de ikke ved noget om (ibid). Det skal virke logisk og naturlig for brugeren (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>3. Bruger-kontrol og frihed </th>
                            <td>
                                Vurdering af hvor meget kontrol brugeren har over hvor de går hen på siden (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>4. Konsistens og standarder </th>
                            <td>
                                Sørger for at designet af siden og dens funktionalitet er ensartet hele vejen igennem siden (ibid). For mindst mulig forvirring (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>5. Forhindring af fejl </th>
                            <td>
                                Sørger websiden for at brugeren ikke ender ud i at gøre en fejl som de vil fortryde og ikke kan gå tilbage fra (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>6. Hellere genkendelse end brug af hukommelse </th>
                            <td>
                                Sørger for at siden bruger konventioner i stedet for at lave sin egen unikke design som brugeren skal til at huske (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>7. Fleksibilitet og effektivitet i brugen </th>
                            <td>
                                Tilbydes der information for både erfarne og uerfarne bruger så deres brugeroplevelse er lige i brugervenlighed eller er det den samme information for begge (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>8. Æstetisk og minimalistisk design </th>
                            <td>
                                Der bør ikke være irrelevante elementer og informationer på siden (ibid). Det skal gerne være minimalistisk og overskueligt (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>9. Hjælp brugeren med at opdage og kommer tilbage efter fejl </th>
                            <td>
                                Giver siden brugeren mulighed for at gå tilbage fra en fejl eller giver dem en meddelelse som simpelt forklarer problemet med en løsning (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>10. Hjælp og dokumentation </th>
                            <td>
                                Webtekster som vejleder brugeren (ibid).
                            </td>
                        </tr>
                        
                    </table>
                </div> <!-- til for at jeg kan indsætte background-image så præcist så muligt -->
            </section> <!-- HERUSTIKKER -->
            
            
            <section class="ekspert-rapport">
                <h2 class="overskrift">Hvad er en eskpertvudering</h2>
                <p class="body-text">
                    I min og Nicolaj Lanks rapport om usability testmetoder har vi lavet en ekspertvudering på Zalandos hjemmeside. Hvor vi gav point ud fra de 10 herustikker og brugte følgende point system fra Usability bogen:
                </p>
                
                <div class="background-image-usability">
                    <table class="usability-table">
                        <tr>
                            <th>1 point </th>
                            <td>
                                Kosmetisk problem, som ikke forhindrer, at websitet kan bruges rigtigt, men som bør rettes, når der er mulighed for det (Gregersen &#38; Wisler-Poulsen, 2017).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>2 point </th>
                            <td>
                                Mindre alvorligt problem, som gør det svært at forstå eller gennemføre handlinger på websitet, og som bør rettes snarest (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>3 point </th>
                            <td>
                                Alvorligt problem, som skaber store problemer i brugen af websitet, og som skal rettes hurtigst muligt (ibid).
                            </td>
                        </tr>
                        
                        <tr>
                            <th>4 point </th>
                            <td>
                                Katastrofalt problem, som gør det umuligt at gennemføre handlinger, og som skal rettes omgående (ibid).
                            </td>
                        </tr>
                    </table>
                </div> <!-- til for at jeg kan indsætte background-image så præcist så muligt -->
                
                
                <section class="systemstatus">
                        <h3 class="underoverskrift">Mangel på systemstatus</h3>

                        <img src="usability/ekspertvudering/3.3.png" alt="screenshot af zalando.dk hvor der er lavet en ekspertvudering" class="ekspert-img">

                        <p class="body-text">
                            Vi fandt frem til, at når man klikker på en kategori i zalandos menu, som t-shirt, så kan man se en brødkrummesti. Dog forsvinder den når man klikker på et produkt. Det gør at kunden har mangel på information om deres placering. Dette er herustik nummer 1: systemstatus.
                        </p>

                        <p class="body-text">
                            Vi kom med forslaget at brødkrummesti altid skull være der helevejen igennem brugerens surfing på siden så de altid er informeret.
                        </p>
