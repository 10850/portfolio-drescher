            <section class="hvad-er-ekspert">
                <h2 class="overskrift">Hvad er en eskpertvudering</h2>
                <p class="body-text">
                    En ekspertvurdering har til formål at lave en systematisk gennemgåelse af et website med eksperter som kender til de standarder og principper som udgør et godt site (Gregersen &#38; Wisler-Poulsen, 2017). Det skal til for at forudsige mulige problemer som kunne formindske brugervenligheden når brugerne interagerer med det (ibid).
                </p>
                
                <p class="body-text">
                    Den ene ekspert er dig selv, og den anden er en ekspert på det domæne/fagområde, som websitet beskæftiger sig med (ibid):
                </p>
                
                <ul class="liste" id="liste1">
                    <li>
                       Den information og de servicer, som websitet tilbyder (ibid). 
                    </li>
                    <li>
                        Den kontekst, som websitet vil blive benyttet i (ibid).
                    </li>
                    <li>
                        De brugere, som vil benytte websitet (ibid).
                    </li>
                </ul>
                
                <p class="body-text">
                    Fordelen ved testen er at den er effektiv og billig at udføre i forhold til en brugertest (ibid). Eksperten kan ikke finde alle fejl fordi der vil altid være overraskelser som kan findes i brugertesten (ibid). Dog ses den stadig som en effektiv og enkel metode (ibid).
                </p>
            </section> <!-- hvad er ekspertvudering -->
            
