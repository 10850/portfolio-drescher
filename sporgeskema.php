                        Hvilke demografiske data kendetegner brugergruppen (Gregersen &#38; Wisler-Poulsen, 2017).
                    </li>
                    
                    <li>
                        Hvilken adgang til og forbrug af internettet har brugergruppen (ibid).
                    </li>
                    
                    <li>
                        Hvilke opgaver løser de på nettet (ibid).
                    </li>
                    
                    <li>
                        Hvilke websites bruger de (ibid).
                    </li>
                    
                    <li>
                        Hvilke konkrete ønsker og behov har de i forhold til websitet (ibid).
                    </li>
                </ul>
            </section> <!-- Typiske emner i et analyserende spørgeskema: -->
            
