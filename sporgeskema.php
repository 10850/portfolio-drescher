            <section class="hvad-er-sporgeskema">
                <h2 class="overskrift">Hvad er et spørgeskema?</h2>
                <p class="body-text">
                    Spørgeskemaer har til formål at karatirisere sin brugergruppe (Gregersen &#38; Wisler-Poulsen, 2017). Man ved hvem websidens primære målgruppe er, men man ved sjældent hvem ens brugergruppe er, og de er nødvendigvis ikke identiske med den primære målgruppe (ibid). Spørgeskemaer egner sig godt til en kvantitativ undersøgelse hvor man undersøger hvor meget, hvor mange, hvor ofte og lignende (ibid). En spørgeskemaundersøgelse er også et godt udgangspunkt til et kvalitativt interview (ibid).
                </p>
            </section> <!-- hvad er spøreskemaer -->
            
            <section class="typiske-emner">
                
                <h3 class="underoverskrift">Typiske emner i et analyserende spørgeskema:</h3>
                
