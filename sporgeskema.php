                        Men endnu mere overraskende er procentdelen af folk der har set andre med symptomber er større end folk der siger de går i skole med symptomber. Er der flere der tager afsted end vi tror? Hvorfor tager de afsted? Er det fordi de vægte uddannelse så højt at de ikke tørre at misse en dag? Er online undervisning ikke en god mulighed for dem? Er det fordi de tror det ikke er så slemt til at det kan være Corona? Det ville være interresant at høre om i et interview.
