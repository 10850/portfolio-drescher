                    <h3 class="underoverskrift">Tager i skole med symptomer?</h3>
                    <p class="body-text">
                        Jeg blev faktisk ret overrasket over de her to statistikker. Jeg troede at siden vi lever i den tid vi nu gør med pandemien, at folk ville blive hjemme når de har symptomer for ikke at putter andre i fare for corona. Så jeg forventet 100% til at man blev hjemme.
                    </p>
                    
