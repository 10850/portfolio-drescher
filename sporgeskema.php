                <ol class="liste" id="forberedelse-list">
                    <li>
                        Brainstorm: skriv ALLE de spørgsmål du kunne forstille der at stille til spørgeskemaet (Gregersen &#38; Wisler-Poulsen, 2017).
                    </li>
                    <li>
                        Gruppér: Inddel alle spørgsmål i grupper så de passer sammen (ibid). Navngiv grupperne hensigtsmæssigt (ibid).
                    </li>
                    <li>
                        Sortér: Undersøg om der en overlappende spørgsmål der kan sættes sammen (ibid). Eller om der er spørgsmål som er mindre vigtige som kan slettes (ibid).
                    </li>
                    <li>
                        Prioritér: Opstil grupperne af spørgsmål i en rækkefølge som virker logisk og naturlig (ibid).
                    </li>
                    
