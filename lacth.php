            <section id="hvad-er-informationsarkitektur">
                <h2 class="overskrift">Hvad er informationsarkitektur</h2>
                <p class="body-text">
                    Strukturer et sites information så brugeren uden problemer kan navigere rundt mellem sidens informationer og ikke behøver at tænke så meget (Andersen, 2015).
                </p>
                <p class="body-text">
                    Bruger er tilbøjelige til at lave deres egen model af hvordan siden virker og bruger siden på den måde til at ”gætte” sig frem (ibid). Man prøver ligesom at leve op til deres forventninger (ibid).
                </p>
            </section>
            
            <section id="latch-def">
                <section id="latch-intro">
                    <h2 class="overskrift">LATCH</h2>
                    <p class="body-text">
                        Der findes 5 måde at lave informationsarkitektur som er kendt som LATCH (How, 2015):
                    </p>
                </section>
                
                <section id="latch-types">
                    <h4 class="sub-skrift">
                        Location
                    </h4>
                    <p class="body-text">
                        Information er organiseret efter deres lokation (ibid). Som med et kort over Danmark, anatomi kort eller nogen blueprints (ibid).
                    </p>
                    
                    <h4 class="sub-skrift">
                        Alphabet
                    </h4>
                    <p class="body-text">
                        Information er organiseret efter alfabetisk orden (ibid). Som med mange ordbøger og telefonbøger (ibid).
                    </p>    
                    
                    <h4 class="sub-skrift">
                        Time
                    </h4>
                    <p class="body-text">
                        Information er organiseret efter tid (ibid). Det kan være oversigt over bustider hvor man ser det tidligste tidspunkt først og det går op (ibid). 
                    </p>
                    
                    <h4 class="sub-skrift">
                        Category
                    </h4>
                    <p class="body-text">
                        Information er organiseret efter kategorier, som er enten allerede sat eller bliver lavet (ibid). Som mange websites bruger og eller på Spotify med musikgenre (ibid).
                    </p>
                    
                    <h4 class="sub-skrift">
                        Hierarchy
                    </h4>
                    <p class="body-text">
                        Information er organiseret i et hierarki, som kan være af vigtighed, mængde eller lignende (ibid). Som en tabel i sports hvor holdet med flest point står øverst, eller en fødekæde hvor dyret som spiser alle andre dyre, er på toppen (ibid).
                    </p>
                </section>
            </section>
            
