                    <p class="body-text">
                        Kvalitative undersøgelser kommer i form af f.eks. Interviews, fokusgrupper og observationsstudier (Busch, 2015). De er kendetegnet ved:
                    </p>
                    <ul class="liste">
                        <li>
                            Man går i dybden med hvorfor folk gøre som de gør (ibid). 
                        </li>
                        <li>
                            Fokusere på individet (ibid).
                        </li>
                        <li>
                            Dybdegående ”hv” spørgsmål (ibid).
                        </li>
                    </ul>
                </section>
                <section id="styrker-kvalitativ">
                    <h3 class="underoverskrift">Stryker</h3>
                    <ul class="liste">
                        <li>
                            God til når man vil teste sit produkt (ibid).
                        </li>
                        <li>
                            Man kan komme mere ind i hovedet på sine kunder og få en dybere forståelse af deres holdninger og behov (ibid).
                        </li>
                        <li>
                            Fleksibilitet med spørgsmål og mulighed for omformulering (ibid).
                        </li>
                        <li>
                            Mulighed for at stille nye spørgsmål on the fly (ibid).
                        </li>
                    </ul>
                </section>
                <section id="svagheder-kvalitativ">
                    <h3 class="underoverskrift">Svagheder</h3>
                    <ul class="liste">
                        <li>
                            Meget mere dyrt og tidskrævende end en kvantitativ undersøgelse (Systime, 2020). 
                        </li>
                        <li>
                            Situationen man sætter op, i høj grad er kunstigt konstrueret situation (Busch, 2015). 
                        </li>
                        <li>
                            Meget omstændig, da man selv kan ændre på situationen på mange måder, som kunne påvirke sin målgruppes svar på sine spørgsmål og på samme tid deres adfærd (ibid). 
                        </li>
                    </ul>
                </section>
            </section>
            
            <section id="kvalitativ-metoder">
                <section id="observationstudie">
                    <section id="observation-intro">
                        <h2 class="overskrift">Observationsstudier</h2>
                        <p class="body-text">
                            Man kigger på det ydre, altså hvad man kan se med det blotte øje (Busch, 2015).
                        </p>
                    </section>
                    
                    <section id="observation-typer">
                        <h4 class="sub-skrift">Deltagende</h4>
                        <p class="body-text">
                            Man er inde i det med testpersonerne og opsamler viden indefra (ibid). Dog med begrænsninger, da man vil være tynget på egen viden, som ændrer ens forudsætninger fra andres (ibid).
                        </p>

                        <h4 class="sub-skrift">Observerende</h4>
                        <p class="body-text">
                            Man kigger udefra og ind på deltagerne så man kan tage noter og nemmere få et mere objektivt billede på det der foregår (ibid). Dog skal gøre sig tanker om hvordan ens metode for observering ændre på deltagernes situation, hvor meget du kan opfange og etisk korrekthed (ibid).
                        </p>

                        <h4 class="sub-skrift">Delvist observerende og deltagende</h4>
                        <p class="body-text">
                            Veksler mellem de to hvis. Hvis man nu ikke har kompetencerne til at gøre de deltagende aktiviteter (ibid).
                        </p>
                    </section>
                </section>
                
                <section id="kvalitativ-interview">
                    <section id="interview-intro">
                        <h2 class="overskrift">
                            Interviews 
                        </h2>
                        <p class="body-text">
                            Interviews er en af de primære metoder i den kvalitative tilgang (ibid). Det skyldes den dybdegående natur med et individ idet man sådan set har en samtale med en anden person (ibid). 
                        </p>
                        <p class="body-text">
                            Hvor man har mulighed for at stille mange forskellige spørgsmål for få indsigt i deres holdninger og adfærd (ibid). Der er mange måde at afholde interviews:
                        </p>
                        <ul class="liste">
                            <li>
                                face to face med individ (ibid).
                            </li>
                            <li>
                                Over telefon (ibid).
                            </li>
                            <li>
                                I en gruppe (ibid). 
                            </li>
                            <li>
                                Semistruktureret - man har nogle emner og stiller spørgsmål ud fra dem og ud fra hvad personen der interviews siger (ibid).
                            </li>
                        </ul>
                    </section>
                    
                    <section id="interview-syv-trin">
                        <h4 class="sub-skrift">
                            7 trin for planlægningen og gennemførelse af et interview (Busch, 2015):
                        </h4>
                        <ol class="liste">
                            <li>
                                Tematisering 	(Formål og emne) 
                            </li>
                            <li>
                                Design 	(Respondenter, forløb, spørgsmålstyper) 
                            </li>
                            <li>
                                Interview 	(Selve interviewet)
                            </li>
                            <li>
                                Transskribering 	(Skriver interviewene ud) 
                            </li>
                            <li>
                                Analyse	(Fortolkning) 
                            </li>
                            <li>
                                Verificering 	(Konklusion)
                            </li>
                            <li>
                                Rapportering 	(Videreformidling) 
                            </li>
                        </ol>
                    </section>
                    
                    <section id="forbehold-interview">
                        <h4 class="sub-skrift">
                            Forbehold ved interviews
                        </h4>
                        <p class="body-text">
                            Der er mange forbehold man skal tage sig for et interview både i det man gøre det og når man skal fortolke det (ibid).
                        </p>
                        <ul class="liste">
                            <li>
                                Valg af spørgsmål (ibid).
                            </li>
                            <li>
                                Præsentere sig rigtigt så personen er komfortabel (ibid).
                            </li>
                            <li>
                                Din egen adfærd under interviewet, som at vise interesse for hvad de har at sige (ibid).
                            </li>
                            <li>
                                Sørg for at omgivelserne er så naturlige så muligt (transskribering uden ekstern påvirkning) (ibid).
                            </li>
                            <li>
                                Kritisk analyse af personens svar og adfærd - kig konkret på hvad der siges (ibid).
                            </li>
                        </ul>
                        <p class="body-text">
                            Interview kendetegnes som forurenet (ibid). Derfor skal man tænke på kontekst, mening, identitet, kulturelle, idealisering og udtrykskontrol, politisk, lokal, og sprogspil når man fortolker sit interview eller endda før det om et interview overhovedet kan bruges (ibid).
                        </p>
                        <p class="body-text">
                            Det skyldes at interviews oftest er en kunstig engangssituation som ikke kan gentages (ibid).
                        </p>
                    </section>
                </section>
            </section>
            
            <section id="fokus-grupper">
                <section id="fokus-grupper-intro">
                    <h3 class="underoverskrift">
                        Fokusgrupper
                    </h3>
                    <p class="body-text">
                        Fokusgrupper er hvor man har et interview/en debat med flere folk fra forskellige kontekster for at få flere øjne på et emne, som vil give flere fortolkninger af samme emne man kan arbejde med (Busch, 2015).
                    </p>
                </section>
                
                <section id="fokus-grupper-involverer">
                    <h4 class="sub-skrift">
                        Fokusgrupper involverer:
                    </h4>
                    <ul class="liste">
                        <li>
                            Debat og perspektiver (ibid).
                        </li>
                        <li>
                            Nye idéer (ibid).
                        </li>
                        <li>
                            3-8 personer (ibid).
                        </li>
                        <li>
                            Udvælg respondenterne med omhu (ibid).
                        </li>
                        <li>
                            Omkring et par timer (ibid).
                        </li>
                        <li>
                            Video, lyd, notater (ibid).
                        </li>
                    </ul>
                </section>
                
                <section id="fokus-grupper-udfordringer">
                    <h4 class="sub-skrift">
                        Udfordringer ved fokus grupper kan være:
                    </h4>
                    <ul class="liste">
                        <li>
                            Kan være vanskelig at holde en rød tråd (ibid).
                        </li>
                        <li>
                            Kan dog give meget data på engang og give flere emner at tænke over som ikke kom op førhen (ibid).
                        </li>
                    </ul>
                    <p class="body-text">
                        Jeg prøvede at lave en fokusgruppe om realityshows og motiverne bag folk som vælger at deltage i dem. Hvor jeg havde en semistruktureret interview forberedt. Jeg fandt ud af at selv om det ikke er fuldt ud planlagt at man stadig kan holde en samtale kørende med spørgsmål fordi de mange perspektiver på et emne ender med at skabe spørgsmål af sig selv. Det sværeste ved fokus grupper var mest at holde den røde tråd og sørger for at være ordstyrer uden at ødelægge flowet af diskussionen.
                    </p>
                </section>
            </section>
            
            <section id="tanke-hojt-test">
                <section id="tanke-hojt-test-intro">
                    <h3 class="underoverskrift">
                        Tænke-højt-test
                    </h3>
                    <p class="body-text">
                        God måde at test prototype i praksis, da man har en person faktisk bruge produktet og man kan nemt observere dem da de taler alt det de tænker (Busch, 2015).
                    </p>
                    <p class="body-text">
                        Der skal dog tages forbehold for, miljø, omstændigheder, personen, distraheringer, hvordan man sætter kamera eller lignende for at observere personen og skærmen for at få det fulde billede (ibid).
                    </p>
                    <p class="body-text">
                        Man skal gøre det så naturligt, autentisk og neutralt så muligt. Da forsøgspersonen nemt kan langsomt komme til bevidstheden om hvad der forgår (ibid).
                    </p>
                    <p class="body-text">
                        Transskribering er vigtigt men skal tages forbehold til hvordan man gøre det (ibid). Det er situationsbestemt (ibid).
                    </p>
                    <p class="body-text">
                        Testen kan være lukket eller åben (ibid).
                    </p>
                    <p class="body-text">
                        Man har mulighed for at lave interview efter testen for eftertanker (ibid).
                    </p>
                    
                    <p class="body-text">
                        Denne metode har jeg brugt mange gange i vores design thinking projekter og har været et godt stykke værktøj til at teste både prototyper og det endelige produkt. Jeg tænkte i starten at folk ville ende med at være helt stille og glemme at sige noget og skete også nogle gange, men i de fleste tilfælde blev der faktisk sagt noget. Og muligheden for interviews efterfølgende er rigtig effektiv når der ikke siges så meget fordi jeg selv ville have observeret noget i testpersonens mimikere eller adfærd på siden, som jeg kunne spørge ind til.
                    </p>
                </section>
