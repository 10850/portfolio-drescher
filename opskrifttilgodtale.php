        <h1 class="titel">Præsentationsteknik</h1>
        
        <div id="mitgrid-godtale">
            
            <section id="multimediedesigner-presentation">
                <p class="body-text">
                    Som multimediedesigner vil man på et eller andet tidspunkt komme ud for at man skal lave en præsentation (Busch, 2015). Det kan være at man skal præsentere resultater fra tests, præsentere løsninger til problemer til en virksomhed, Pitch en ide til nogen eller det kan være et eksternt oplæg for din kunde (ibid). Uanset hvilken situation det er skal der være et fundament med gode principper indenfor præsentationsteknik hvis man gerne vil gøre sig bedst egnet til at udtrykke sit budskab (ibid).
                </p>
            </section>
            
            <section id="opskrift-tale">
                <section id="inventio">
                    <h3 class="underoverksrift">
                        Inventio
                    </h3>
                    
                    <p class="body-text">
                        Her får du alt stoffet som skal være med i talen forberedt, det er både det du skal sige og illustrationer du måske vil vise (Busch, 2015). Man skal også finde ud af hvad skal præsentationen handle om? Hvad er formålet? (ibid) De tre kategorier af taler, som hver har sit formål:
                    </p>
                    
                    <ol class="liste">
                        <li>
                            Festtalen, der har til formål at hylde eller dadle og fokuserer på nuet (ibid).
                        </li>
                        <li>
                            Den politiske tale, der skruer fremad og har til formål at bringe fremtidige perspektiver i spil (ibid).
                        </li>
                        <li>
                            Den juridiske tale, der skruer bagud og vurderer, om noget er rigtigt eller forkert (ibid).
                        </li>
                    </ol>
                </section>
                
                <section id="dispositio">
                    <h3 class="underoverksrift">
                        Dispositio 
                    </h3>
                    
                    <p class="body-text">
                        Her disponerer du indholdet, altså du organiserer dit oplæg og lægge det i en rækkefølge, som får delene til at følge logisk efter hinanden (Busch, 2015). Strukturen i talens dele er i klassisk retorik:
                    </p>
                    
                    <ol class="liste">
                        <li>
                            Exordium: indledningen (ibid). 
                        </li>
                        <li>
                            Narratio: sagsfremstillingen, fortællingen om sagen, den gode historie (ibid).
                        </li>
                        <li>
                            Argumentatio: bevisførelsen, hvor du kan trække på Toulmins klassiske (ibid). argumentationsteori om påstand, belæg og hjemmel (ibid).
                        </li>
                        <li>
                            Peroratio: afslutningen (ibid).
                        </li>
                    </ol>
                </section>
                
                <section id="elocutio">
                    <h3 class="underoverksrift">
                        Elocutio 
                    </h3>
                    <p class="body-text">
                        Her giver du talen en sporlig udformning (Busch, 2015): 
                    </p>
                    <ol class="liste">
                        <li>
                            Aptum: hensigtsmæssigt, relevant, tilpasset modtagers forhåndsviden (ibid).
                        </li>
                        <li>
                            Puritas: Sprogligt korrekt (ibid).
                        </li>
                        <li>
                            Perspicuitas: Klart og forståeligt (ibid).
                        </li>
                        <li>
                            Ornatus: levende, varieret, afstemt stil (ibid).
                        </li>
                    </ol>
                </section>
                
                <section id="memoria">
                    <h3 class="underoverksrift">
                        Memoria 
                    </h3>
                    <p class="body-text">
                        Her er det øvelse, øvelse og mere øvelse så du kan talen udenad og øver dig på hvordan du vil fremtræde (Busch, 2015).
                    </p>
                </section>
                
                <section id="actio">
                    <h3 class="underoverksrift">
                        Actio
                    </h3>
                    <p class="body-text">
                        Tid til at præsentere! (Busch, 2015).
                    </p>
                </section>
            </section>
            
            <section id="fisk-model">
                <section id="fisk-model-intro">
                    <h3 class="underoverskrift">Fisk modellen</h3>
                    <p class="body-text">
                        Fiske modellen er en klassisk måde at disponere sin præsentation (Busch, 2015).
                    </p>
                </section>
                
                <section id="fisk-begynd">
                    <h4 class="sub-skrift">
                        Begyndelse:
                    </h4>
                    <p class="body-text">
                        Her skal man fange modtagernes interesse og gøre dem nysgerrige (ibid). Det gøres ved at man starter konkret og simpelt og giver publikummet overblik over hvad præsentationen indeholder og hvad den har til formål at gøre (ibid). Det kan også være en strategi at komme med et provokerende udsagn som vækker deres nysgerrighed (ibid).
                    </p>
                </section>
                
                <section id="fisk-midt">
                    <h4 class="sub-skrift">
                        Midt: 
                    </h4>
                    <p class="body-text">
                        Her folder man indholdet ud og bruger det meste af din taletid her (ibid). Man kommer med sine hovedkonklusioner af analysen, argumenter og belæg, vise kundskab og viden (ibid). Afrund med gode pointer eller spørgsmål så oplægget virker helstøbt (ibid).
                    </p>
                </section>
                
                <section id="fisk-afslut">
                    <h4 class="sub-skrift">
                        Afslutning: 
                    </h4>
                    <p class="body-text">
                        Halen starter med at være snævret og konkret hvor du så igen åbner op for dine pointer som afrunding (ibid).  Man skal gå efter at får hoved og hale til at have sammenhæng (ibid).  Det kan gøres ved at man ved hovedet stillet et spørgsmål og man så ved halen referer tilbage til det spørgsmål og svarer på det (ibid).
                    </p>
                </section>
            </section>
            
