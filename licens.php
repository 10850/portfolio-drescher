                </h2>
                <p class="body-text">
                    GDPR er Databeskyttelsesforordningen/Persondataforordningen. GDPR står for General Data Protection Regulation, som var lavet af EU. Den har siden 2018 gjaldt for alle virksomheder (Rijna, 2020).
