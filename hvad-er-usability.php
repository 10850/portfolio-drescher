            <section class="skitster">
                <h2 class="overskrift">Skitser og prototyper</h2>
                <p class="body-text">
                    Skitser kan være en tegning af en persons forestilling af det de vil udvikle eller videreudvikle på (Gregersen &#38; Wisler-Poulsen, 2017). Den er simpel at lave og man har ikke noget imod at tage chancer da man nemt kan erstattes eller blive lavet igen (ibid).
                </p>
                <p class="body-text">
                    Prototyper er hvor man laver et produkt i lavere kvalitet end det man gerne vil ende med at lave (ibid). Det kunne for eksempel være hvis man skulle lave en taske, så ville man ikke lave prototypen af dyrt læder eller lignende (ibid). Det gør også at man har nemmere ved at se kritisk på sit arbejde så man kan forbedre ens prototype, så man ikke bliver utilfreds med sit endelige produkt (ibid).
                </p>
            </section> <!-- Skitser -->
            
