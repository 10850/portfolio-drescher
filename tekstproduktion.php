                        Nyhedskriterier 
                    </h2>
                    <p class="body-text">
                        For at en nyhed er en nyhed skal historien opfylde en eller flere af disse nyhedskriterier:
                    </p>
                    
                    <ol class="liste">
                       <li>
                            Væsentlighed, så noget som har en væsentlig konsekvens eller betydning for mange mennesker. Ex: Skatten stiger næste år (Schmith, 2020).
                        </li>
                        <li>
                            Sensation, dvs. noget der fascinerer eller chokerer (ibid). Ex: Mand bider hund eller Kvinde overlevede en måned under sammenstyrtet hus (ibid).
                        </li>
                        <li>
                            Konflikt, dvs. når sager eller personer er på kollisionskurs (ibid). Ex: Vred far anklager kommune for sjusk (ibid).
                        </li>
                        <li>
                            Identifikation, dvs. noget som læseren kan genkende sig selv i (ibid). En historie, der får læseren til at tænke ’Det kunne være mig’, ’Bare det var mig’ eller ’Godt, det ikke er mig’ (ibid). Ex: Ni ud af ti danskere spiser for meget sukker (ibid).
                        </li>
                        <li>
                            Aktualitet, dvs. noget der er oppe i tiden (ibid). Historien må gerne udspringe af stof som i forvejen cirkulerer i andre medier (ibid). Langt de fleste nyheder udspringer af dette nyhedskriterium. Ex: Et parti vælger ny formand eller et containerskib er sejlet ind i en bro (ibid).
                        </li>
                    </ol>
