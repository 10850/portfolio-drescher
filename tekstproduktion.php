                <h2 class="overskrift">
                    En ny måde at skrive på
                </h2>
                
                <p class="body-text">
                    Jeg havde allerede lært om nyhedstrekanten og brochure og annoncer før vi havde om tekstproduktion så jeg troede ikke der ville være så meget at lære. Men så kom Webtekster, Sociale medier tekster, HeyYouSeeSo og de gode råd til at skrive kort, simpelt og overskueligt tekst til web og det åbnede øjnene op for mig for hvor forskelligt en nyhedsartikel og tekst på internettet er og hvor forskellige brugerne er i de to genrer. Jeg begyndte faktisk at se mig selv i de situationer med at være utålmodig og bare skanner tekster før jeg vælger at gå videre til næste side, og blev mere opmærksom på hvordan forskellige sider og influencers skriver deres tekster. Jeg vidste ikke at tekstproduktion på web ville have sådan en størrelse før semesteret startet og kan nu se hvor bredt et emne tekstproduktion er alt efter hvilken kontekst man befinder sig i. Jeg har også lært via øvelserne på klassen og K3 opgaver selv at skrive/omskrive tekster til at være mere bruger- og SEO venlige. 
                </p>
                
                <p class="body-text">
                    Jeg kan ikke sige jeg er en ekspert, men jeg har mere forstand på hvad man skal tage forbehold til nu end jeg gjorde da jeg først startede på semestret hvor jeg førhen havde haft meget med skrivning af nyhedsartikler, annoncer og andre former for skrifter, men var aldrig bevidst om hvor forskelligt det er at skrive på web.
                </p>
