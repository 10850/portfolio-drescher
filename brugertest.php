                <section class="refleksion-bruger">
                    <h2 class="overskrift">Hvad jeg lærte fra brugertesten</h2>
                    <p class="body-text">
                        Jeg har aldrig prøvet at arrangere eller lave en brugertest før og tænkte at det ville være svært at få folk til at tænke højt for mig så jeg kunne notere det ned.
                    </p>
                    <p class="body-text">
                        Overraskende nok så har det ikke været så besværeligt at få dem til at tale, både under rapporten og i design thinking projekterne. Og selv når nogen var lidt tavse var jeg stadig istand til at få lavet observationer jeg kunne spørg indtil efter og fik mere indblik på personen.
                    </p>
                    <p class="body-text">
                        Jeg er faktisk lidt overrasket over hvor meget indblik man kan få på hvem ens brugere er og hvilke problemer ens side har bare ved at lave 3 brugertests. Jeg tænkte at man normalt ville lave en hjemmeside efter visse standarder og lave ændringer efterfølgende når den var lagt ud, men mængden af information man kan få ud af brugerne, selv hvis de er subjektive holdninger er meget større end jeg vil tro. Og det er ikke engang fordi jeg testede dem særlig længe, det var kun en test på en 15-20 min.
                    </p>
                </section>
                
