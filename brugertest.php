                            <li>
                                Skærmoptagelse
                            </li>
                            <li>
                                Billeder 
                            </li>
                        </ul>

                        <p class="body-text">
                            Udover det er det vigtigt at forberede forholdene for at testpersonen er i den mest rolige situation muligt (ibid). Det skal føles som om de sidder i deres eget hjem, så deres adfærd og meninger ikke bliver manipuleret af omstændighederne (ibid). Det kunne være at man vil have dejlige møbler i et stort rum, som har noget drikkelse med (ibid). Det kunne også være man laver testen på brugerens egen computer (ibid).
                        </p>
                        
                        <p class="body-text">
                            Der er to måder man kan opstille de praktiske forhold i forhold til antal mennesker og hvor de skal side (ibid). Det kan være low-budget eller high-budget:
                        </p>
                        
