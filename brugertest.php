                <section class="hvad-er-brugertest">
                    <h2 class="overskrift">Hvad er en Brugertest?</h2>

                    <p class="body-text">
                        Brugertesten også kendt som brugervenlighedstesten har til formål at teste hvor bruger venligt en webside er og er den mest anerkendte usability-testmetode (Gregersen &#38; Wisler-Poulsen, 2017). 
                        Man tester om brugeren kan bruge websitet til det den er blevet lavet til og hvordan deres brugeroplevelse er (ibid). I brugertesten er det realistisk at gennemføre en ”tænke højt test” hvor brugeren siger alt de tænker mens de bruger siden. Derved kan man få indblik på deres adfærd, forventninger og holdninger (ibid).

                    </p>

                    <p class="body-text">
                        Brugertesten bruges til et informerer eller bekræfte. Det vil sige informere om hvilke problemer der er og bekræfte om de problemer faktisk er relevante (ibid). Det specielt vigtigt når man skal bekræfte at der er et problem til andre (ibid).
                    </p>
                </section>
                
                <section class="forbered-bruger">
                    <section class="forbered-intro">
                        <h2 class="overskrift">Forberedelse</h2>

                        <p class="body-text">
                            Brugertesten er en meget ressourcekrævende test og det kommer af forberedelsesfasen (Gregersen &#38; Wisler-Poulsen, 2017). Når man skal lave en brugertest, er der tre faser man går igennem hvor forberedelsen fylder mest:
                        </p>

                        <ul class="liste">
                            <li>
                                2 til 3 uger til forberedelse (ibid).
                            </li>
                            <li>
                                1-2 dage til udførelse (ibid).
                            </li>
                            <li>
                                4-5 dage til efterbehandling og dokumentation (ibid).
                            </li>
                        </ul>
                    </section>
                    
                    <section class="de-tre-bruger">
                        <h3 class="underoverskrift">De tre overordnede punkter</h3>
                        <ul class="liste">
                            <li>
                                Forberedelse af testopgaver og praktiske forhold (ibid).
                            </li>
                            <li>
                                Rekruttering af testdeltagere (ibid).
                            </li>
                            <li>
                                Udførelse af pilottest (ibid).
                            </li>
                        </ul>
                    </section>
                    
                    <section class="design-testopgave">
                        <h3 class="underoverskrift">Design af testopgaver</h3>

                        <p class="body-text">
                            Når man laver en brugertest, skal man vælge et fokus for testen for at holde på kvaliteten af testen og dens resultat (ibid). Derfor vil man sørge for at man tester de 3 vigtigste features (ibid).
                        </p>
                    </section>
                    
                    <section class="overveje-testopgave">
                        <h3 class="underoverskrift">Overvejelser til den gode testopgave</h3>

                        <ul class="liste">
                            <li>
                                At opgaven er mulig at løse (ibid).
                            </li>
                            <li>
                                At opgaven er relevant og realistisk. Så at den kontekst opgaven dækker er en realistisk situation, som brugeren kunne se sig selv i (ibid).
                            </li>
                            <li>
                                At det er realistisk sekvens - så ikke opdigtet (ibid).
                            </li>
                            <li>
                                At opgaven er beskrevet med et endeligt og forudsigeligt sæt mulige af løsninger (ibid).
                            </li>
                            <li>
                                At der er et tydeligt slut-punkt, som brugeren kan genkende (ibid).
                            </li>
                            <li>
                                At opgaven opfordrer til handling og interaktion med sitet og ikke spørg om deres mening (ibid).
                            </li>
                            <li>
                                At opgaven er af realistisk længde. Man skal ikke lade brugeren gå kold og kede sig (ibid).
                            </li>
                            <li>
                                At den er beskrevet med skjult hjælp. Man kan guide brugeren ved at give dem hints til hvad de skal gøre på siden, men det skal ikke lige ud og ret sige været enkelt step de skal igennem (ibid).
                            </li>
                        </ul>
                    </section>
                    
                    <section class="Opgaveskabelonen">
                        <h3 class="underoverskrift">Opgaveskabelonen </h3>

                        <p class="body-text">
                            Opgaveskabelonen er til for at formålet med opgaven bliver beskrevet og at opgaven er gennemtænkt (Gregersen &#38; Wisler-Poulsen, 2017). I en opgaveskabelon vil der dækkes over:
                        </p>

                        <ol class="liste">
                            <li>
                                Det mål eller den problemstilling, der danner grundlag for testen (ibid).
                            </li>
                            <li>
                                Hypoteser eller bruger, som man forventer at se, eller som skal udfordres (ibid).
                            </li>
                            <li>
                                De skridt eller funktioner, man forventer, at brugeren gennemgår for at løse opgaven (ibid).
                            </li>
                            <li>
                                Den tid man forventer brugeren vil bruge på opgaven (ibid). Det er godt til at sammenligne efterfølgende og fortolke på om tiden de bruger reflektere hvor intuitivt siden er (ibid).
                            </li>
                            <li>
                                Selve opgaveteksten (ibid).
                            </li>
                            <li>
                                Felt til noter (ibid).
                            </li>
                        </ol>
                    </section>
                    
