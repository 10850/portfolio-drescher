                            </li>
                            <li>
                                <strong>Beardline</strong> - viser basen af uppercase bogstaverne (ibid).
                            </li>
                            <li>
                                <strong>Bar</strong> - Den horisontale linje i bogstaver som T, A, H, e, f osv. (ibid).
                            </li>
