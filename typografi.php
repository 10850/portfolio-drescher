                        <h2 class="overskrift">Hvorfor typografi?</h2>
                        <p class="body-text">
                            Typografi er ikke kun kommunikation via de ord man skriver, men også essentiel til visuel kommunikation (Rold, 2019). Forskellige fonts giver en vis mening og stemning ligesom farver kan (ibid).
                        </p>
                        <h4 class="sub-skrift">Der findes 3 overordnede skrifttyper man taler om i typografi:</h4>
                        <ul class="liste">
