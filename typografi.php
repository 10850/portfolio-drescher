                    <section id="skrifttyper-passer">
                        <h2 class="overskrift">Skrifttyper skal passe til indhold og fortæller historier</h2>
                        <p class="body-text">
                            Valg af skrifttype er vigtig for læserens opfattelse af hvad man læser, for eksempel om man vælger sans Serif, som virker mere moderne eller Serif som virker mere klassisk (Rold, 2019).
                        </p>
                        <p class="body-text">
                            Ligesom kropssprog og mimiker er det vigtig at de er konkurrent og passer sammen (ibid). Det er til læserens opfattelse og fortolkning af hvad de læser (ibid). Hvis man for eksempel har en ballet sko man vil sælge på sit website og vil man vælge en font som fremstilles meget elegant som en form for kursiv script font (ibid). Der er ikke noget rigtigt eller forkert svar, men det skal passe sammen med indholdet (ibid).
                        </p>
                    </section>
                    
                    <section id="typografi-komposition">
                        <h2 class="overskrift">Typografisk komposition</h2>
                        <p class="body-text">
                            Vores opgave er at formidle information, ikke at være kunstnere (Rold, 2019). Der er spilleregler for hvad er bedst at bruge ved traditionelt print og andre for digital fremvisning (ibid):
                        </p>
                        <ol class="liste">
                            <li>
                                Typografiens størrelse skal være fleksibel i tilfælde hvilken platform man bruger til at kigge på desktop, mobil, tablet etc. (ibid).
                            </li>
                            <li>
                                Læseadfærd skifter mellem traditionel printet tekst som er lineært læsende og web som er meget non-lineært læst og mange gange har man tilbøjelighed til at multitaske (ibid).
                            </li>
                        </ol>
                    </section>
                    
                    <section id="skrifttyperne">
                        <h3 class="underoverskrift">Skrifttyper</h3>
                        <h4 class="sub-skrift">Serif</h4>
                        <p class="body-text">
                           Serifer er karakterisere med deres fødder og er gode til store mængder af tekst, grundet deres fødder (Rold, 2019). De hjælper også med at gruppere bogstaver så de laver en samlet form, som er godt fordi mennesket når man har at gøre med store mængder tekster læser mere efter former end enkelte bogstaver (ibid). Serifer skaber også en vandret læser form som holder bedre på vores fokus (ibid).
                        </p>
                        
                        <h4 class="sub-skrift">Sans serif</h4>
                        <p class="body-text">
                            Sans serifer er karaktiriseret af at de ikke har fødder, hentydet ved ordet sans = uden (Rold, 2019). De er gode til korte tekstmængder, specielt overskrifter og titler (ibid). Sans serifer på grund den ikke har fødder og bogstaverne typisk har samme stregtykkelse hele vejen omkring bogstavet, som får dem til at fremstå mere tidligere og klare og hurtigt læseligt (ibid).
                        </p>
                        
                        
                        <h4 class="sub-skrift">Monospace</h4>
                        <p class="body-text">
                            Optager samme plads mellem bogstaverne (Rold, 2019).
                            God til når bogstaverne skal tydeliggøres ligeligt, end teksten som helhed (ibid). Det kunne bruges ved for eksempel et password (ibid).
                        </p>
                        
                        <h4 class="sub-skrift">Script</h4>
                        <p class="body-text">
                            Karakteriseret ved dens realistiske look, da de er baseret på virkelighedsnære håndskrift (Rold, 2019).
                        </p>
                    </section>
