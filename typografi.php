                            </li>
                            <li>
                                <strong>Descender</strong> - Den del af bogstavet som går under baselinen ved lowercase bogstaver (ibid).
                            </li>
                            <li>
                                <strong>Ascender</strong> - Den del af bogstavet som går over meanlinen ved lowercase bogstaver (ibid).
