                    </li>
                </ul>
            </section>
            
            <section id="imk-modellen">
                <section id="imk-intro">
                    <h2 class="overskrift">IMK-modellen</h2>
                    <p class="body-text">
                        International Markeds Kommunikations-modellen, sætter teksten, som også forstås som produktet i fokus (Busch, 2015). Kontekst, kultur, medie og genre påvirker alle teksten/produktet. Modtageren og afsenderen påvirker også kommunikationssituationen, da modtagerens syn også skal tages med i udviklingen for at få den ønskede effekt (ibid).
                    </p>
                </section>
                
                <section id="imk-tre-aspekter">
                    <h3 class="underoverskrift">
                        Tre væsentlige teoretiske aspekter: 
                    </h3>
                    <ul class="liste">
                        <li>
                            Både afsender (produktion) og modtager (reception) skal deltage før der er en interaktion (Busch, 2015). Modtager og afsender har muligvis ikke samme koder og ser derfor produktet forskelligt (ibid).
                        </li>
                        <li>
                            Den genre man bruger til produktet, gør at produktet opfattes ligesom de andre i den genre, og bliver kodet derefter (ibid). Det hedder et diskursfællesskab og findes ved analyse af tekstens trækstruktur og retoriske strategi (ibid).
                        </li>
                        <li>
                            Modellen opfatter alle kulturelle fænomener som tegn (ibid). Det kaldes for en semiotisk tilgang (ibid).
                        </li>
                    </ul>
                </section>
                
                <img src="kommunikation/Paradigmer/imk-modellen.png" alt="imk-modellen" id="imk-modellen-img">
                
                <section id="imk-elementer">
                    <h3 class="underoverskrift">
                        Modellens elementer:
                    </h3>
                    <ul class="liste">
                        <li>
                            <strong>Afsender:</strong> Afsenderen af kommunikationen. Kan både være producenten (teksteksterne) og den der har bestilt produktet (tekstinterne) (Busch, 2015). Primær afsender er den der har bestilt produktet, mens den sekundære kan være specialister fremhævet i produktet (ibid).
                        </li>
                        <li>
                            <strong>Modtager:</strong> Modtageren af kommunikationen. Kan både være specifik målgruppe (tekstinterne) eller uden for målgruppen (teksteksterne) (ibid). Primær modtager er dem der specifikt bruger produktet, mens sekundære er dem der kan påvirke den primære modtager til at bruge det (ibid).
                        </li>
                        <li>
                            <strong>Teksten:</strong> Selve kommunikationen (ibid).
                        </li>
                        <li>
                            <strong>Referenten:</strong> Alt hvad teksten refererer til. Kan være et specifikt produkt, et brand osv. (ibid).
                        </li>
                        <li>
                            <strong>Koden:</strong> Hvad man bruger til at give teksten et bestemt budskab (ibid). Kan være det medie der bliver brugt, konteksten det bliver set i, og hvilken genre der bliver brugt (ibid).
                        </li>
                        <li>
                            <strong>Kontekst/kultur:</strong> Situationen som kommunikationen sker i, altså hvem, hvad, hvor og hvornår. Her spiller modtagerens kultur også en rolle (ibid).
                        </li>
                        <li>
                            <strong>Medier:</strong> Hvordan kommunikationen bliver præsenteret. Kan være forskellige former for print eller elektronisk (ibid). Her tænkes situationen og anvendelsen ind over for at vælge det mest passende medie (ibid).
                        </li>
                        <li>
                            <strong>Genre:</strong> Hvordan kontekst/kultur, medier og retoriske strategier spiller sammen (ibid):
                        </li>
                        <li>
                            <strong>Kommunikative formål:</strong> ses både fra afsender og modtagers perspektiv (ibid).
                        </li>
                        <li>
                            <strong>Trækstrukturer:</strong> de sproglige træk der hjælper med at opfylde kommunikationens formål, fx: specifikke træk fra en avisartikel eller reklame annonce (ibid).
                        </li>
                        <li>
                            <strong>Retoriske strategier:</strong> virkemidler til at få trækkene til at være mere tydelige (ibid). Kan både være sprogligt eller visuelle (ibid). 
                        </li>
                    </ul>
                </section>
            </section>
            

            <section id="thorlacius-modellen">
                <section id="thorlacius-intro">
                    <h2 class="overskrift">Thorlacius’ kommunikationsmodel</h2>
                    <p class="body-text">
                        En model der forklarer både visuelle/æstetiske og funktionelle aspekter af webkommunikation (Busch, 2015).
                    </p>
                </section>
                
                <img src="kommunikation/Paradigmer/thorlacius.png" alt="thorlacius kommunikationsmodel" id="thorlacius-model-img">
                
                <section id="thorlacius-elementer">
                    <h3 class="underoverskrift">
                        Modellens elementer:
                    </h3>
                    <ul class="liste">
                        <li>
                            <strong>Faktisk afsender:</strong> Den person som får produktet lavet (Busch, 2015).
                        </li>
                        <li>
                            <strong>Implicitte afsender:</strong> Den person der ses som afsender via produktet. Kan findes via en receptionsanalyse af modtagerne (ibid).
                        </li>
                        <li>
                            <strong>Faktisk modtager:</strong> Den modtager der faktisk bruger produktet. Kan findes via en konkret receptionsanalyse (ibid).
                        </li>
                        <li>
                            <strong>Implicitte modtager:</strong> Den modtager som produktet er tiltænkt. Kan findes via en analyse af kommunikationsproduktet (ibid).
                        </li>
                        <li>
                            <strong>Produktet:</strong> Selve produktet. Er både udtrykket og indholdet af produktet (ibid).
                        </li>
                        <li>
                            <strong>Konteksten:</strong> Sammenhængen produktet indgår i (ibid). Både den situationelle- og tekstlige kontekst, samt referentielle- og intertekstuelle funktion skal undersøges her (ibid).
                        </li>
                        <li>
                            <strong>Mediet:</strong> Mediet som produktet bliver udtrykt gennem (ibid).
                        </li>
                        <li>
                            <strong>Koden:</strong> Hvad man bruger til at give teksten et bestemt budskab. Både metakommunikative (taler om sig selv)- og intersemiotiske (bruger et kodesystem til at beskrive et andet) funktioner skal bruges her (ibid).
                        </li>
                    </ul>
                </section>
