                <section id="mapping">
                    <section id="hvad-er-mapping">
                        <h3 class="underoverskrift">
                            Hvad er mapping?
                        </h3>
                        
                        <p class="body-text">
                            Mapping er et teknisk begreb for at funktionen lever op til brugerens forventninger (ibid). 
                        </p>
                        
                        <p class="body-text">
                            God mapping er når funktionerne/knapperne på siden gør det som brugerne forventer når de bruger den. Ligesom når man kører bil og man bruger rettet, hvis man roterer rettet mod højre, så drejer bilen til højre og vice versa (ibid). 
                        </p>
                        
                        <p class="body-text">
                            Dårlig mapping vil selvfølgelig så være når brugeren bruger en funktion på siden og den ender med at gøre noget man ikke forventet. Som når man skal styre en båd og skal skubbe rorpinden til højre for at gå til venstre så inverse controls (ibid). 
                        </p>
                        
                        <p class="body-text">
                            Mapping handler om at gøre brugerens brugeroplevelse så naturlig og intuitiv som overhovedet muligt (ibid).
                        </p>
                    </section>
