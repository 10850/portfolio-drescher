                    <section id="empathize-intro">
                        <h2 class="designthinking-section-overskrift">
                            Empathize
                        </h2>
                        <p class="designthinking-bodytext">
                            Empathize er den første fase, og er her man skal gøre sig nysgerrig for menneskers behov, adfærd og kontekst. Man kan researche på nettet, læse i bøger eller artikler eller gå ud og interviewe selve målgruppen for et mere menneskeligt indblik på tingene. 
                        </p>
                    </section> <!-- empathize intro -->
                    
                    <img src="design-thinking/interview-empathize.jpg" alt="Billed fra Empathize fasen hvor jeg gik ud og intervievet folk" id="empathize-interview" class="dt-img"> <!-- billed af empathize i praksis -->
                    
                    <section id="empathize-praksis">
                        <h3 class="designthinking-section-underoverskrift">
                            Empathize på studiet
                        </h3>
                        <p class="designthinking-bodytext">
                            I Hackthon med Skive Festival og startede jeg med at gøre mig nysgerrig på selve organisationen via research. Her indgår det at kigge på deres egen hjemmeside, tidligere events de har holdt og hvordan deres marketing var på forskellige sociale medier og kigge på hvad for et budskab og hvilket billede de gerne vil fremstille om sig selv for andre. 
                        </p>
                        <p class="designthinking-bodytext">
                            Derefter gik jeg ud til kunderne/deltagerne i deres festival for at gøre mig nysgerrig på deres adfærd før, til og efter festivalen, traditioner de måske har, spørger indtil hvorfor de tager afsted, hvad de finder værdifuldt og hvilken kontekst de er i som hvor de bor, om de har venner og familie de ikke ser så tit osv.  
                        </p>
                    </section> <!-- empathize i praksis -->
                </section> <!-- empathize -->
                
