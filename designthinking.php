                    <section id="ideate-intro">
                        <h2 class="designthinking-section-overskrift">
                            Ideate
                        </h2>
                        <p class="designthinking-bodytext">
                            Ideate er der hvor det ”sjove” begynder, og man endelige får lov til at åbne op for alle de ideer man har haft gennem empathize og define fasen. I denne fase tager man problemformuleringen fra define fasen og man udforsker alle de potentielle ideer til en løsning ved divergent tænkning.
                        </p>
                    </section> <!-- ideate intro -->
                    <section id="ideate-ideer">
                        <h3 class="designthinking-section-underoverskrift">
                            Der findes ikke gode eller dårlige ideer
                        </h3>
                        <p class="designthinking-bodytext">
                            Alle vil højst sandsynligt have en favorit-ide, men i denne fase er det vigtigt at man ikke bliver for snævret i sit syn. Man skal være åben for alle muligheder og derfor siger man i design thinking at der ikke findes noget som en dårlig eller god ide, det er bare mulige løsninger man kan kigge på.
                        </p>
                    </section> <!-- Ingen dårlige eller gode ideer -->
                    
