                    Design Thinking
                </h1>
                
                <img src="design-thinking/designthinking-faser.png" alt="billed af Design Thinking faserne" id="dt-faser-mobil" class="dt-img"> <!-- billed af faserne for mobil -->
                
                <section id="hvad-er-designthinking">
                    <h2 class="designthinking-section-overskrift">
                        Hvad er Design Thinking?
                    </h2>
                    <p class="designthinking-bodytext">
                        Design Thinking er en tilgang til hvordan man skal tænke og bære sig ad med at løse et problem. 
                    </p>
                    
                    <p class="designthinking-bodytext">
                        Det er en iterativ proces hvor der opfordres til at man gør sig nysgerrig, researcher og analysere flere forskellige mulige løsninger før man går ind og udvikler og implementere den endelige løsning. I Design Thinking går man gennem 5 faser, som skifter mellem at være bredde til konkrete, og på samme måde skal man også skifte mellem divergent- og konvergent tænkning. 
                    </p>
                    
                    <p class="designthinking-bodytext">
                        Indsigt i og nysgerrighed på menneskers behov, adfærd og kontekst er afgørende for design thinking, fordi det giver både problemforståelse og udvikling af innovative meningsfulde og værdiskabende løsninger.
                    </p>
                </section> <!-- hvad er design thinking -->
                
                <section id="empathize">
                    
