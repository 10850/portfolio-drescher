                            Optimering
                        </h3>
                        <p class="designthinking-bodytext">
                            Optimering er, the name of the game, I protype fasen og indebærer også at det er en iterativ proces, hvor man laver flere prototype rundter, hvor man retter til for hver rundte. Indtil man har et endeligt billed af hvordan løsningen skal implementeres. 
                        </p>
                    </section> <!-- optimering -->
                    
                    
