                    <section id="ideate-brainstorm">
                        <h3 class="designthinking-section-underoverskrift">
                            Brainstorming 
                        </h3>
                        <p class="designthinking-bodytext">
                            Måden vi har arbejdet med ideate har været ved brainstorming og negativ brainstorming, hvor vi går gennem iterative processer af brainstorming:
                        </p>
                        <ul class="designthinking-lister" id="ideate-liste">
                            <li class="brainstorm-list-item">1 rundte hvor vi sidder for os selv og skriver ideer på labels og derefter sætter alt på bordet. Det er for at få alle de logiske ideer ud. </li>
                            <li class="brainstorm-list-item">1 rundte hvor vi kigger på alle de skrevende ideer og får nye og måske innovative ideer som er mere ude af boksen. </li>
                            <li class="brainstorm-list-item">1 og sidste rundte hvor vi diskuterer alle ideer på bordet og gøre det til noget konkret vi kan tage videre til prototypefasen. Her ville vi mange gange få nye ideer og blande forskellige ideer sammen. For eksempel med Hackathon hvor plakaten var en ide, sloganet var en anden ide og corona kit var en helt tredje ide.</li>
                        </ul>
                    </section> <!-- brainstorming -->
