                            Prototype + Test
                        </h2>
                        <p class="designthinking-bodytext">
                            Prototype fasen begynder man på udviklingen af sin ide, men man skal huske at en prototype er en fysisk repæsentation af en ide, som kan kommunikere udvalgte aspekter af ideen til relevante mennesker med henblik på feedback. 
                        </p>
                    </section> <!-- prototype intro -->
