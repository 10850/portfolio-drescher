                                <strong>Lilla:</strong> denne farve er brugt på højre side af hjernen hvor jeg har UI emner og på design siden fordi lilla associeres med kreativitet, mystik og fantasi. Som passer godt til den kreative del af hjernen hvor man gerne ville være innovativ og tænke udenfor boksen og bøje reglerne lidt.
