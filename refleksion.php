                                <strong>Blå:</strong> denne farve er til UX emner eller dem ved venstre side af hjernen, det er fordi blå associeres med troværdighed og loyalitet, så jeg tænkte det passer godt til emner som er standarsiret, set in stone og er bygget på at man altid følger teorier som er videnskabeligt bevidste og bekræftet.
