                                <strong>Rød:</strong> denne farve er også brugt på højre side og brugt på videoproduktion siden, det er fordi rød associeres med passion og stærke følelser. Det passer godt til mig som elsker at lave videoer og at videoer og film mange gange har det mål at få folk til at føle noget i mange forskellige ekstremer.
