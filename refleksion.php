                    <section id="kodning-refleksion-portfolio">
                        <h2 class="overskrift">Kodning</h2>
                        <p class="body-text">
                            I kodning brugte jeg intet javascript selv, men bare bootstrap til menuerne på siden.
                        </p>
                        <p class="body-text">
                            Jeg brugte PHP include til mine navigationer og footer som gjorde det meget nemmere fordi jeg ikke skulle inde og ændre på linksene heletiden når jeg kopier det ind i en ny HTML-fil med en unik filplacering. Det endte så med at alle filer har samme placering, så der er ikke nogen mappestruktur fordi include virkede ikke hvis filerne lå i forskellige steder. Der er fordi menuens links ville ikke tage udgangspunkt i index.php’ens placering med den aktuelle fils placering som så gøre at den ikke kan finde de andre sider uden en ../.
                        </p>
                        <p class="body-text">
                            Så jeg skulle ofre den mappestruktur, men det var det værd for det gjorde meget nemmere og hurtigere. 
                        </p>
                        <p class="body-text">
                            Jeg prøvede at lave alt CSS for alle sider på en CSS-fil for SEO, da der ikke er flere CSS filer serveren skal holde styr på. Det var udfordrende men jeg lærte hvordan man kunne have struktur i sin CSS med så mange side på en gang med kommentarer og med en ny class til hver sides body tag for at sikre det ikke påvirket andre sider. 
                        </p>
                        <p class="body-text">
                            Jeg blev også klogere længere hen i kodningsprocessen, hvor jeg for eksempel fandt ud at jeg med mine skrifttyper, liste og noget padding på visse elementer, kunne lave en class som jeg lavet til en absolut styling der galdte for alle elementer der havde den class. Det gjorde meget mere nemmere og reddet plads i CSS’en fordi jeg nu ikke skulle gøre det samme css igen og igen for hver eneste php-fil.
                        </p>
                        <p class="body-text">
                            Jeg sørgeret for at mit design ikke var så kompleks så jeg nemt kunne lave siden med det jeg har lært så CSS-grid.
                        </p>
                        <p class="body-text">
                            Jeg endte også med at skifte min måde at arbejde på fra at jeg lavede en side af gangen til at arbejde step by step med flere sider på en gang. Jeg fandt ud af efter nogle dage at fokusere på en side af gangen gik alt for langsomt og jeg ville ende med at bruge mere tid end planlagt på små problemer. Så jeg skiftet til at arbejde på mange sider på en gang som var under samme kategori i min informationsarkitektur og det gjorde processen meget hurtigere og det var nemmere for at holde på en tidsplan fordi jeg ikke ville lade mig sidde fast i små detaljer og i stedet vendte med dem til når jeg blev færdig og fik tid til finpudsning.
                        </p>
