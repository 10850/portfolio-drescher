                                Farver
                            </h3>
                            <p class="body-text">
                                Jeg kigger nu på farverne og jeg fik inspiration fra et billede på google af en illustration af UX/UI-design med den højre og venstre side af hjernen, hvor den venstre side var sort med computer wiring og den højre side var med mange forskellige farver og shapes. 
