                <section id="kvantitativ-intro">
                    <h2 class="overskrift">Karakteristika (styrker og svagheder)</h2>
                    <p class="body-text">
                        Kvantitative undersøgelser er kendetegnet ved:
                    </p>
                    <ul class="liste">
                        <li>
                            Fokuserer på mængder (Busch, 2015). 
                        </li>
                        <li>
                            Fokuserer på hvor mange der gør noget og meget konkret på hvem det er (ibid). 
                        </li>
                        <li>
                            Laver en mere generel fremstilling af målgruppen f.eks. alder, køn og boligpæl (Systime, 2020). 
                        </li>
                        <li>
                            Brug af lukkede spørgsmål, da man nemmere kan indlæse data når svarene er korte, konkrete og konsistent (ibid).
                        </li>
                    </ul>
                    <a href="kommunikation/Empiri/Opgave-om-metoderne-forfalder.pdf" target="_blank" class="pdf-link">
                        Denne information og informationen på resten af siden uddybbes mere i min rapport om kvalitativ og kvantitativ empiri.
                    </a>
                </section>
                <section id="styrker-kvantitativ">
                    <h3 class="underoverskrift">Stryker</h3>
                    <ul class="liste">
                        <li>
                            Hurtigt, nemt og billigt kan indsamle data (Systime, 2020).
                        </li>
                        <li>
                            Indsamle troværdige data i den sans at hvis mængden af deltager er stor så er sandsynligheden for at resultatet er korrekt også større (ibid).
                        </li>
                        <li>
                            Godt udgangspunkt til at drage generelle konklusioner som gælder for mange (ibid).
                        </li>
                        <li>
                            Man kan nemt finde hvem sin målgruppe eller brugergruppe (Busch, 2015). Grundet dens meget konkrete data som er let sammenligneligt (ibid).
                        </li>
                        <li>
                            Godt udgangspunkt til en kvalitativ undersøgelse hvis man er et nyt firma eller et firma der skal lave en undersøgelse fra bunden af (ibid).
                        </li>
                    </ul>
                </section>
                <section id="svagheder-kvantitativ">
                    <h3 class="underoverskrift">Svagheder</h3>
                    <ul class="liste">
                        <li>
                            Anonymiteten som typisk er en del af denne tilgang (Busch, 2015). Kan gøre at man aldrig rigtig ved om folk egentlig er hvem de udgiver sig for at være (ibid).
                        </li>
                        <li>
                            Ingen analyse af kropssprog (ibid).
                        </li>
                        <li>
                            Man kender ikke deltagernes omstændigheder, som kunne have indflydelse på deres svar (ibid). 
                        </li>
                        <li>
                            Man skal kunne formulere spørgsmålene med omhu. For bare fordi du forstår det betyder ikke alle andre vil forstå det på samme måde (ibid). 
                        </li>
                        <li>
                            Neutrale svar, som ved ikke eller hverken eller. Det kan fører til problemer med efterbehandlingen af dataet når man skal tolke på det (ibid). 
                        </li>
                        <li>
                            Afhængig af undersøgerens færdigheder og forståelse for sin målgruppe (Systime, 2020). Da man ikke er ansigt til ansigt, og har chance for at omformulere sig (ibid). 
                        </li>
                    </ul>
                </section>
            </section>
            
