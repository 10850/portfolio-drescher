                        <h2 class="overskrift">Spørgeskemaundersøgelse </h2>
                        <p class="body-text">
                            Spørgeskemaer er standardmåden man lave kvantitative undersøgelser, da den hurtigt og let kan indsamle masser af dat (Busch, 2015). Der er forskellige måder man kan afholde spørgeskemaundersøgelser (ibid): 
                        </p>
                        <ul class="liste">
                            <li>
                                Over telefon (ibid).
                            </li>
                            <li>
                                Ansigt til ansigt (ibid).
                            </li>
                            <li>
                                Over nettet (ibid).
                            </li>
                        </ul>
                    </section>
                    
                    <section id="sporgskema-desig-kvantitativ">
                        <h4 class="sub-skrift">Design af spørgeskemaer</h4>
                        <p class="body-text">
                            Når man designer sit spørgeskema, er der forbehold man skal tage sig i opbygningen af spørgeskemaet (Busch, 2015):
                        </p>

                        <ul class="liste">
                            <li>
                                Have en klar og snæver problemstilling (ibid).
                            </li>
                            <li>
                                Pas på med bredde begreber der kan forstås på forskelligvis (ibid).
                            </li>
                            <li>
                                Hav en klar ide om hvad svarene skal bruges til (ibid).
                            </li>
                            <li>
                                Vær bevidst om hvilke typer spørgsmål man stiller f.eks. faktuelle spørgsmål (ibid).
                            </li>
                            <li>
                                Overvej hvilke baggrundsspørgsmål der er vigtige at stille (ibid).
                            </li>
                            <li>
                                Hvordan skal spørgeskemaet præsenteres (ibid).
                            </li>
                            <li>
                                Overvej svars kategorier i forhold til det givende spørgsmål (ibid).
                            </li>
                            <li>
                                Være bevidst om at kontekst spiller en rolle i hvilke svar du får og tænk over hvilken kontekst folk muligvis er i når de svar på spørgsmålene (ibid).
                            </li>
                        </ul>
                        
